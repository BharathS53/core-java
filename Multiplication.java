public class Multiplication {
    public static void main(String[] args) {
      int i=3,k=0;
      for(int j=1;j<=10;j++){
        k=i*j;
        System.out.println(i +"X"+ j +"="+ k);
      }
System.out.println("\n");
      for(int a=1;a<=10;a++){
        int b=6,c=0;
        c=a*b;
        System.out.println(b+"x"+a+"="+c);
      }
System.out.println("\n");
      for(int x=1;x<=10;x++){
        int y=9,z=0;
        z=x*y;
        System.out.println(y+"x"+x+"="+z);
      }
    }
}